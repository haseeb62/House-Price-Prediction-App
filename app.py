import streamlit as st
import pandas as pd
from sklearn.linear_model import LinearRegression
from sklearn.tree import DecisionTreeRegressor
data = pd.read_csv('house_data.csv')

# Train the model
X = data[['Area', 'Bedrooms', 'Bathrooms', 'Age', 'Location_Score']]
y = data['Price']
model_lr = LinearRegression()
model_lr.fit(X, y)

model_dt = DecisionTreeRegressor()
model_dt.fit(X, y)

# Main Title
st.title("AI House Price Prediction App")

selected_model = st.selectbox("Select Model", ["Linear Regression", "Decision Tree Regressor"])

col1, col2 = st.columns(2)
#1st House Details
st.subheader("House 1 Details")
area1 = st.number_input("Area (sq ft)", key="area1")
bedrooms1 = st.number_input("Bedrooms", key="bedrooms1")
bathrooms1 = st.number_input("Bathrooms", key="bathrooms1")
age1 = st.number_input("Age of the house (years)", key="age1")
location_type1 = st.selectbox("Location Type", ["Urban", "Suburban", "Rural"], key="location_type1")

#2nd House Details
st.subheader("House 2 Details")

area2 = st.number_input("Area (sq ft)", key="area2")
bedrooms2 = st.number_input("Bedrooms", key="bedrooms2")
bathrooms2 = st.number_input("Bathrooms", key="bathrooms2")
age2 = st.number_input("Age of the house (years)", key="age2")
location_type2 = st.selectbox("Location Type", ["Urban", "Suburban", "Rural"], key="location_type2")

# Initialize results so they're always defined
result1 = None
result2 = None

if st.button("Compare Prices"):
    # Predict price for House 1
    if location_type1 == "Urban":
        location_score1 = 1
    elif location_type1 == "Suburban":
        location_score1 = 2
    else:
        location_score1 = 3

    # Predict price for House 2
    if location_type2 == "Urban":
        location_score2 = 1
    elif location_type2 == "Suburban":
        location_score2 = 2
    else:
        location_score2 = 3

    if selected_model == "Linear Regression":
        choose_model = model_lr
    else:
        choose_model = model_dt

    result1 = choose_model.predict([[area1, bedrooms1, bathrooms1, age1, location_score1]])
    result2 = choose_model.predict([[area2, bedrooms2, bathrooms2, age2, location_score2]])

    st.success(f"Predicted Price for House 1: {result1[0]:,.0f} PKR")
    st.success(f"Predicted Price for House 2: {result2[0]:,.0f} PKR")
    st.write("Note: The predictions are based on a simple linear regression model and may not reflect actual market prices.")
    st.write("Disclaimer: This app is for educational purposes only and should not be used for actual real estate transactions.")

    st.subheader("Save Your Results")
    report_content = f"""AI House Price Prediction Report
    AI Model Used: {selected_model}
House 1: {result1[0]:,.0F} PKR
House 2: {result2[0]:,.0F} PKR
Generated by AI House Price Prediction App.
"""

    st.download_button(
        label = "Download Text Report",
        data = report_content,
        file_name = "house_price_comparison_report.txt",
        mime = "text/plain"
    )
else:
    st.write("Note: The predictions are based on a simple linear regression model and may not reflect actual market prices.")
    st.write("Disclaimer: This app is for educational purposes only and should not be used for actual real estate transactions.")
